#ifndef NA62_BUFFER_H
#define NA62_BUFFER_H

// Definitions in the block header (generic) only:
#define DATA_BLOCK_FORMAT_MASK 0xff000000
#define DATA_BLOCK_FORMAT_RSHIFT 24
#define DATA_SOURCE_SUB_ID_MASK 0x00ff0000
#define DATA_SOURCE_SUB_ID_RSHIFT 16
#define DATA_BLOCK_SIZE_MASK 0x0000ffff
#define DATA_BLOCK_SIZE_RSHIFT 0
#define DATA_BLOCK_TIMESTAMP_MASK 0xffffffff
#define DATA_BLOCK_TIMESTAMP_RSHIFT 0

// Various masks and functions for trigger extraction
#define L0_SPECIAL_TRIGGER_MASK 0x00000080
#define L0_SOB          0x00000088
#define L0_EOB          0x0000008c
#define L0_CHK_ERR_MASK 0x00000090  //choke_on=10010000, choke_off=10010100, error_on=10011000, error_off=10011100
#define L0_CHOKE_ON     0x00000090
#define L0_CHOKE_OFF    0x00000094
#define L0_SYNC         0x00000080
#define L0_MONITORING   0x000000a0
#define L0_TRIGGER_TYPE_MASK 0x000000ff
#define L0_TRIGGER_TYPE_RSHIFT 0

//Definition for DIMEOB HEADER
#define DIM_SPECIAL_TRIGGER_NUMBER_OF_WORDS 0x0000ffff
#define DIM_SPECIAL_TRIGGER_SUBID 0xff000000
#define DIM_SPECIAL_TRIGGER_DATABLOCK 0x00ff0000
#define DIM_SPECIAL_TRIGGER_SUBID_RSHIFT 24
#define DIM_SPECIAL_TRIGGER_DATABLOCK_RSHIFT 16
#define DIM_SPECIAL_TRIGGER_NUMBER_OF_WORDS_RSHIFT 0
#endif // NA62_BUFFER_H
