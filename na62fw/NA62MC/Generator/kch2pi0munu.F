        idhep(2) = -13    ! mu+
        idhep(4) = 14     ! nu

        call photos(1)
        nhep_after = nhep
c              something new in the decay  ????
c              if yes record the new parameters
c              in MCLIST common block
c
        if(nhep_after.gt.nhep_before)   then
          nhep_rad = nhep_after - nhep_before ! number of rad particles
C.
c         update parameters of the decay products in MCLIST
c
          do j = 1, 4
            ppi(j) = phep(j,3)
            pmu(j) = phep(j,2)
            pnu(j) = phep(j,4)
C.
C.        add radiative gammas in MCLISt
c
            if (nhep_rad.ge.1) PRAD1(j) = phep(j,5)
            if (nhep_rad.ge.2) PRAD2(j) = phep(j,6)
            if (nhep_rad.ge.3) PRAD3(j) = phep(j,7)
            if (nhep_rad.ge.4) PRAD4(j) = phep(j,8)
