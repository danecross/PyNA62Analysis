          enddo
        endif !  end nhep_after > nhep_before
C
***********************************************************
***          End of PHOTOS2 rad. corr. evaluations      *** 
***********************************************************
*
        P_mu=sqrt(pmu(4)**2-RLMAS2)
        E_pi_min=0.5*(RKMASS-pmu(4)-P_mu+PIMAS2/(RKMASS-pmu(4)-P_mu))
        E_pi_max=0.5*(RKMASS-pmu(4)+P_mu+PIMAS2/(RKMASS-pmu(4)+P_mu))
