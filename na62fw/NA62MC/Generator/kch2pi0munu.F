        jmohep(1,4) = 1
        jmohep(2,4) = 0
        jdahep(1,4) = 0
        jdahep(2,4) = 0
        phep(1,4) = pnu(1)
        phep(2,4) = pnu(2)
        phep(3,4) = pnu(3)
        phep(4,4) = pnu(4)
        phep(5,4) = 0.0
        do i = 1,4
          vhep(i,4) = 0.
        enddo

