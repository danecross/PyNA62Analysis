      IF (nhep_rad.ge.4) then
        JG = MCADD4GEN(IDGAM,PRAD4,2)
        CALL DBOOST(p4ini(1,jkaon),MKCH,PRAD4,PRAD4)
      ENDIF
      IF (nhep_rad.ge.5) then
        JG = MCADD4GEN(IDGAM,PRAD5,2)
        CALL DBOOST(p4ini(1,jkaon),MKCH,PRAD5,PRAD5)
      ENDIF

      JPION = MCADD4(IPION, PPI)
      JMUON = MCADD4(IMUON, PMU)
      if (nhep_rad.ge.1) JG = MCADD4(IDGAM,PRAD1)
      if (nhep_rad.ge.2) JG = MCADD4(IDGAM,PRAD2)
      if (nhep_rad.ge.3) JG = MCADD4(IDGAM,PRAD3)
      if (nhep_rad.ge.4) JG = MCADD4(IDGAM,PRAD4)
      if (nhep_rad.ge.5) JG = MCADD4(IDGAM,PRAD5)

C --- FORCED PI0 DECAY

      pzmode = mod (pi0decay, 100)
      call pi0decay_manager(jpion, ppi, pzmode)

      RETURN
      END
